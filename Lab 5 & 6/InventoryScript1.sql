create table item_cat_code1(
  ItemCatCodeKey  integer not null,
  ItemCatCodeId   varchar(4) not null,
  ItemCatDesc     varchar(30) not null,
  constraint      item_cat_code1_PK Primary Key(ItemCatCodeKey)
